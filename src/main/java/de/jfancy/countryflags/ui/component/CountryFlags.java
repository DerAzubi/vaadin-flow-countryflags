    private String url;
    private String alt;

    CountryFlags(String url, String alt) {
        this.url = url;
        this.alt = alt;
    }

    public Image create() {
        Image image = new Image(url, alt);
        image.getStyle().set("vertical-align", "middle");
        return image;
    }

    /**
     * Addes some margin to the right
     * 
     * @param marginRight Amount in px
     * @return
     */
    public Image create(int marginRight) {
        Image image = create();
        image.getStyle().set("margin-right", marginRight + "px");
        return image;
    }

    public Image create(int marginRight, int width, int height) {
        Image image = create();
        image.setWidth(width + "px");
        image.setHeight(height + "px");
        image.getStyle().set("margin-right", marginRight + "px");
        return image;
    }

